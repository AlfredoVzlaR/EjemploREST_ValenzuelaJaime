package entidades;

/**
 *
 * @author jvale
 */
public class Producto {
    private int id;
    private String nombre;

    public Producto(int id, String nobmre) {
        this.id = id;
        this.nombre = nobmre;
    }

    public Producto() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNobmre() {
        return nombre;
    }

    public void setNobmre(String nobmre) {
        this.nombre = nobmre;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 29 * hash + this.id;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Producto other = (Producto) obj;
        return this.id == other.id;
    }
    
    
    
}
